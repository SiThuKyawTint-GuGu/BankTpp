<script setup>
import { computed } from 'vue';

const emit = defineEmits(['update:checked']);

const props = defineProps({
    checked: {
        type: [Array, Boolean],
        default: false,
    },
    value: {
        default: null,
    },
    labeltxt: {
        default: null,
    }

});

const proxyChecked = computed({
    get() {
        return props.checked;
    },

    set(val) {
        emit('update:checked', val);
    },
});
</script>

<template>
    <v-checkbox type="checkbox" :value="value" v-model="proxyChecked" :label="labeltxt"></v-checkbox>
</template>
